<h4 class="text-center"><strong>Common Livestock Illnesses: Symptoms,Treatment and Prevention</strong></h4>
<h5><strong>Contagious Diseases</strong></h5>
<p><strong>Foot and Mouth Disease</strong> – Foot and mouth disease is a highly contagious disease in livestock that causes lesions similar to blisters on the tongue, nose, mouth and toes of the animals. The disease does not normally kill the livestock but creates painful ulcers after the blisters burst, and can cause high fevers, weakened legs, and even the loss of the ability to walk. Luckily, strains of foot and mouth disease has not existed in the United States since 1929.</p>
<p><strong>Brucellosis and tuberculosis</strong>– Brucellosis and tuberculosis are two highly contagious diseases carried by cattle. Similar in nature, these diseases are spread by infected material during calving. The diseases threaten humans, because the unpasteurized milk of infected cattle can spread the disease. To avoid the chances of cattle being infected by brucellosis and tuberculosis ensure cattle are bred and brought in from a reliable source, do not borrow or lend calving equipment, and isolating pregnant or infected animals.</p>
<p><strong>Vaccinations</strong>– Vaccinations exist to protect livestock from diseases such as black leg, malignant edema, red water, enterotoxaemia, black disease, and leptospirosis.</p>
<hr>
<h5><strong>Dehydration and heat stress</strong></h5>
<p>Dehydration and heat stress are common issues livestock face during the hot summer months.</p>
<ul><strong>Signs of Livestock dehydration:</strong>
    <li>Sunken eyes</li>
    <li>“Skin tent” after pinching skin</li>
    <li>Yellow, dark urine</li>
    <li>Fever</li></ul>
<p class="text-justify">Treating dehydration requires rehydration of the animal with plenty of water and electrolytes. On average, cattle need anywhere from 3-30 gallons of water per day to stay hydrated. It is safe for a cow to drink 5-10 gallons of water per intake.</p>
<p class="text-justify">Heat stress is shown by a rectal temperature of more than 102.5 F. To decrease the effects of heat stress in livestock, keep the animals hydrated with plenty of water. An automatic animal water system from Nelson Manufacturing ensures livestock always have access to fresh, clean water. Provide multiple areas for water to limit competition amongst livestock. Additionally, make shade available to the animals so they are able to escape the sun, and spray down the animals or set up sprinklers when the temperature exceeds 80 degrees.</p>
<hr>
<h5><strong>Anaemia – Theileria</strong></h5>
<p class="text-justify">Anaemia in animals is often caused by bush ticks that attach themselves to the livestock. Normally found in the ears, around the tail, and on the underside of the animal, bush ticks transfer, especially to cattle, a disease known as theileriosis. The disease is common among young calves 8-12 weeks old, but can affect cattle of all ages. The disease creates symptoms similar to anaemia in humans: lethargy, loss of appetite, inability to exercise. Cattle with anaemia may have pale/yellow colored gums and abort babies if pregnant.</p>
<p class="text-justify">A blood sample is the only way to determine whether or not livestock is affected by Theileria. To treat affected animals, blood transfusion are preformed to ensure improvement in valuable livestock. In livestock with less value, reducing the stress and movement of the animal and providing high quality feed may help them recover, although death is possible.</p>

<p class="text-justify"><strong>To prevent anaemia</strong> – theileria, examine cattle regularly, especially young cattle. Additionally, reducing tick population in the area that livestock live and graze is important in protecting the animals from bush ticks. Pesticides such as acaricide can reduce the number of ticks found in an area.</p>
<hr>
<h5><strong>Bracken Poisoning</strong></h5>
<p class="text-justify">Bracken poisoning most commonly occurs in the fall when grass does not grow. Bracken is a plant that contains toxins poisonous to animals who graze. Bracken is considered cancerous and is almost always fatal to infected livestock. When an animal eats a large amount of bracken, disease forms because of depressed bone marrow and lack of production of white blood cells. Death can occur as soon as five days after the first sign of symptoms.</p>

<ul><strong>Common Symptoms include:</strong>
    <li>Depression and loss of appetite</li>
    <li>Bloody diarrhea</li>
    <li>High temperature</li>
    <li>Weakening, collapse and death</li></ul>

<small>Herbicides and burning of pastures help prevent the outbreak of bracken.</small>



