<h3 class="text-center"><strong>Plants for different soil types</strong></h3>
<p class="text-justify">Have you ever tried to grow a plant which just simply doesn’t thrive? It could be due to your soil type! We all have slightly different soil depending on where we live, from dry sandy soil to heavy, wet clay. Many plants just can’t cope with the extreme conditions these soils provide, which is why it’s important to assess your soil before planting. It’s also a good idea to consider your soil pH (acidity or alkalinity) as this will affect certain plants too. Read on to learn all about your soil type and find plants suitable for your land.</p>

<p class="text-justify"><strong>What type of soil do I have?</strong><br>
Soil type (texture) is defined by the percentage of clay, silt and sand it contains. These three ingredients form a ‘loam’. There are many variations of loam so you may well find your soil falls somewhere in-between the types described below.</p><hr>

<p class="text-justify"><strong>Clay soil</strong><br>
Clay soils are composed of lots of tiny mineral particles which reduces the air space in the soil. This causes the soil to retain water and become easily compacted. You can tell if you are gardening on clay soil as it will be sticky, heavy and easily moulded into a shape when rubbed between your fingers. If you have hard soil in the garden during the summer this could be another clue that you have clay soil. Dry weather causes clay soil to bake hard and crack on the surface. These soils are very fertile but require some improvement to ‘unlock’ the nutrients for plants to use.</p>

<p class="text-justify"><strong>Silt soil</strong><br>
Silt soils have slightly bigger particles than clay soils so are more free-draining but are still at risk of compaction. They are fertile and drain more effectively than clay but hold more moisture than sandy soils. You can tell if you are gardening on a silty soil as it can be moulded into a shape but will feel smooth rather than sticky. It won’t hold its shape as easily as clay and will crack or crumble if rolled too thinly between the palms of your hand.</p>

<p class="text-justify"><strong>Sandy soil</strong><br>
Sandy soils have the largest particles (up to 1000 times bigger than clay) and are very free-draining due to the big air spaces between particles. You can tell if you are gardening on a sandy soil as it will feel light and gritty and cannot be moulded into a shape. Although easy to work and cultivate, sandy soils dry out very easily and often have poor fertility as the nutrients are simply washed away.</p>











