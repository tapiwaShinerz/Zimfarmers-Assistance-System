<h3 class="text-center"><strong>Signs and symptoms of plant disease: Is it fungal, viral or bacterial ?</strong></h3>
<div class="text-center">Familiarity with the way plant diseases are visually identified can help you diagnose problems.</div><hr>
<p class="text-justify">Most plant diseases – around 85 percent – are caused by fungal or fungal-like organisms. However, other serious diseases of food and feed crops are caused by viral and bacterial organisms. Certain nematodes also cause plant disease. Some plant diseases are classified as “abiotic,” or diseases that are non-infectious and include damage from air pollution, nutritional deficiencies or toxicities, and grow under less than optimal conditions. For now, we’ll look at diseases caused by the three main pathogenic microbes: fungus, bacteria and virus. If plant disease is suspected, careful attention to plant appearance can give a good clue regarding the type of pathogen involved.<p>

<p class="text-justify">A sign of plant disease is physical evidence of the pathogen. For example, fungal fruiting bodies are a sign of disease. When you look at powdery mildew on a lilac leaf, you’re actually looking at the parasitic fungal disease organism itself (Microsphaera alni). Bacterial canker of stone fruits causes gummosis, a bacterial exudate emerging from the cankers. The thick, liquid exudate is primarily composed of bacteria and is a sign of the disease, although the canker itself is composed of plant tissue and is a symptom.</p>

<p class="text-justify">A symptom of plant disease is a visible effect of disease on the plant. Symptoms may include a detectable change in color, shape or function of the plant as it responds to the pathogen. Leaf wilting is a typical symptom of verticilium wilt, caused by the fungal plant pathogens Verticillium albo-atrum and V. dahliae. Common bacterial blight symptoms include brown, necrotic lesions surrounded by a bright yellow halo at the leaf margin or interior of the leaf on bean plants. You are not actually seeing the disease pathogen, but rather a symptom that is being caused by the pathogen.</p>

<p>Here are a few examples of common signs and symptoms of fungal, bacterial and viral plant diseases:</p>
<ul><strong>Fungal disease signs:</strong>
    <li>Leaf rust (common leaf rust in corn)</li>
    <li>Stem rust (wheat stem rust)</li>
    <li>Sclerotinia (white mold)</li>
    <li>Powdery mildew</li></ul>

<ul><strong>Fungal disease symptoms:</strong>
    <li>Birds-eye spot on berries (anthracnose)</li>
    <li>Damping off of seedlings (phytophthora)</li>
    <li>Leaf spot (septoria brown spot)</li>
    <li>Chlorosis (yellowing of leaves)</li></ul>
<hr>

<p><strong>Bacterial disease signs (difficult to observe, but can include):</strong></p>
   <ul><li> Bacterial ooze</li>
    <li>Water-soaked lesions</li>
    <li>Bacterial streaming in water from a cut stem</li></ul>

<ul><strong>Bacterial disease symptoms:</strong>
    <li>Leaf spot with yellow halo</li>
    <li>Fruit spot</li>
    <li>Canker</li>
    <li>Crown gall</li>
    <li>Sheperd’s crook stem ends on woody plants</li></ul><hr>

<p><strong>Viral disease signs: None – the viruses themselves can’t be seen</strong></p>
<ul><strong>Viral disease symptoms:</strong>
    <li>Mosaic leaf pattern</li>
    <li>Crinkled leaves</li>
    <li>Yellowed leaves</li>
    <li>Plant stunting</li></ul>




